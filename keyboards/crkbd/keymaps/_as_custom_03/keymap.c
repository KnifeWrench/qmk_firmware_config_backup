void keyboard_post_init_user(void) {
    // Initialize EEPROM if uninitialized
    if (!eeconfig_is_enabled()) {
        eeconfig_init();
    }

    // Load stored setting (0 for MAC_MODE, 1 for WIN_MODE)
    uint8_t os_mode = eeconfig_read_user();
    if (os_mode == MAC_MODE) {
        layer_move(_BASE_MAC); // Switch to macOS layer
    } else {
        layer_move(_BASE_WIN); // Switch to Windows layer
    }
}

