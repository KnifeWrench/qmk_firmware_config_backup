/* KEYBOARD PET STATUS START */
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
        case SWITCH_TO_MAC:
            if (record->event.pressed) {
                layer_move(_BASE_MAC);
                eeconfig_update_user(MAC_MODE); // Save setting to EEPROM
            }
            return false;
        case SWITCH_TO_WIN:
            if (record->event.pressed) {
                layer_move(_BASE_WIN);
                eeconfig_update_user(WIN_MODE); // Save setting to EEPROM
            }
            return false;
    }
    return true;
};
